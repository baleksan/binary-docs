public class BinaryFormatConverterException extends Exception {
    public BinaryFormatConverterException() {
        super();
    }

    public BinaryFormatConverterException(String message, Throwable cause) {
        super(message, cause);
    }

    public BinaryFormatConverterException(String message) {
        super(message);
    }

    public BinaryFormatConverterException(Throwable cause) {
        super(cause);
    }
}
